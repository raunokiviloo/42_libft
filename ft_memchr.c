#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t bytestocheck)
{
	char	*string;

	string = (char *)str;
	while (*string && bytestocheck-- > 0)
	{
		if (*string++ == c)
			return (string - 1);
	}
	return (NULL);
}
